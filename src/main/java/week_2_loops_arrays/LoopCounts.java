package week_2_loops_arrays;

public class LoopCounts {
    public static void main(String[] args) {
        //print numbers 1-5
        for (int x = 1 ; x <= 5 ; x++)
            System.out.println(x);
        // print 10, 9, 8, ...0
        for (int x = 10 ; x >= 0 ; x--)
            System.out.println(x + "...");
    }
}
